object scalaoperators {
  def main(args: Array[String]) {
    val a = "John"
    val b = "Jhon"

    println("a == b = " + (a == b) )
    println("a != b = " + (a != b) )
    println("a > b = " + (a > b) )
    println("a < b = " + (a < b) )
    println("b >= a = " + (b >= a) )
    println("b <= a = " + (b <= a) )
  }
}
